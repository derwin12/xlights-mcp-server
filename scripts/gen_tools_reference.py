def _is_tool_decorator(node) -> bool:
    """Return True if `node` is @mcp.tool() or @mcp.tool."""
    if isinstance(node, ast.Attribute) and node.attr == "tool":
        return True
    if (isinstance(node, ast.Call)
            and isinstance(node.func, ast.Attribute)
            and node.func.attr == "tool"):
        return True
    return False


def extract_tools(path: pathlib.Path) -> dict[str, dict]:
    """Return {tool_name: {description, params}} for every @mcp.tool() in path."""
    src = path.read_text(encoding="utf-8")
    tree = ast.parse(src)
    tools = {}

    for node in ast.walk(tree):
        if not isinstance(node, ast.FunctionDef):
            continue
        if not any(_is_tool_decorator(d) for d in node.decorator_list):
            continue

        doc = ast.get_docstring(node) or ""
        # First non-empty line of docstring
        first_line = next((l.strip() for l in doc.splitlines() if l.strip()), "")

        # Collect parameter names (skip self, host, port — low-level plumbing)
        params = []
        for arg in node.args.args:
            if arg.arg not in ("self", "host", "port"):
                params.append(arg.arg)

        tools[node.name] = {"description": first_line, "params": params}

    return tools


def build_section(tools: dict[str, dict]) -> str:
    known = set()
    lines = ["## Available Tools", ""]
    lines.append(
        "_This section is auto-generated by `scripts/gen_tools_reference.py`. "
        "Do not edit by hand._"
    )
    lines.append("")

    for heading, emoji, names in GROUPS:
        group_tools = [(n, tools[n]) for n in names if n in tools]
        if not group_tools:
            continue
        known.update(n for n, _ in group_tools)

        lines.append(f"### {emoji} {heading}")
        lines.append("")
        lines.append("| Tool | Description |")
        lines.append("|------|-------------|")
        for name, info in group_tools:
            desc = info["description"].replace("|", "\\|")
            lines.append(f"| `{name}` | {desc} |")
        lines.append("")

    # Catch any tools not assigned to a group
    ungrouped = [(n, t) for n, t in tools.items() if n not in known]
    if ungrouped:
        lines.append("### 🔧 Other")
        lines.append("")
        lines.append("| Tool | Description |")
        lines.append("|------|-------------|")
        for name, info in ungrouped:
            desc = info["description"].replace("|", "\\|")
            lines.append(f"| `{name}` | {desc} |")
        lines.append("")

    total = len(tools)
    lines.append(f"_Total: {total} tools_")
    lines.append("")
    return "\n".join(lines)


def update_readme(readme: pathlib.Path, section: str) -> bool:
    """Replace the block between TOOLS markers with `section`. Returns True if changed."""
    text = readme.read_text(encoding="utf-8")

    start = text.find(START_MARKER)
    end = text.find(END_MARKER)

    if start == -1 or end == -1:
        print(f"ERROR: markers not found in {readme}", file=sys.stderr)
        print(f"  Add '{START_MARKER}' and '{END_MARKER}' to README.md", file=sys.stderr)
        sys.exit(1)

    new_block = f"{START_MARKER}\n{section}{END_MARKER}"
    new_text = text[:start] + new_block + text[end + len(END_MARKER):]

    if new_text == text:
        return False

    readme.write_text(new_text, encoding="utf-8")
    return True


def main():
    tools = extract_tools(SERVER_PY)
    section = build_section(tools)
    changed = update_readme(README, section)

    if changed:
        print(f"README.md updated — {len(tools)} tools across {len(GROUPS)} groups.")
    else:
        print(f"README.md already up to date ({len(tools)} tools).")


if __name__ == "__main__":
    main()
