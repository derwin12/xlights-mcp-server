#!/usr/bin/env python3
"""Generate the Available Tools reference section in README.md.

Parses server.py via AST, groups every @mcp.tool() by category, and
rewrites the README block between the <!-- TOOLS:START --> and
<!-- TOOLS:END --> markers.

Run manually:
    python scripts/gen_tools_reference.py

Or add as a pre-commit hook:
    # .pre-commit-config.yaml
    - repo: local
      hooks:
        - id: gen-tools-reference
          name: Update tools reference in README
          entry: python scripts/gen_tools_reference.py
          language: python
          pass_filenames: false
"""

